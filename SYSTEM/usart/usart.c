u8 check_frame1=0;//帧头1校验标志
u8 check_frame2=0;//帧头2校验标志
u16 data_len=0;//接收到数据长度
u16 pageIndex=0;//存储页面
u16 bagIndex=0;//当前第几包
u16 sumBag=0;
u32 writeAddrFlash=0;
 u8 sendbuf[7]={0x5A,0xA5,0x04,0x05,0xFF,0x42,0xA0};//回复成功
int USART1_IRQHandler(void)
{    
    if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET) //Check if data is received 
    {
        u8 Usart_Receive;
        static u16 Count;
        static u8 rxbuf[MAX_REC_LEN];
       
        int check=0,i,j;
        
        Usart_Receive = USART_ReceiveData(USART1); //Read the data 
     
        //Fill the array with serial data
        rxbuf[Count] = Usart_Receive;  
        
       if(check_frame1==1&&check_frame2==1) Count++;
        //判断帧头
			if(Usart_Receive == FRAME_HEADER1 ||check_frame1==1)
			{
				 check_frame1=1;
				 if(Usart_Receive == FRAME_HEADER2||check_frame2==1) 
				 {
					
					 check_frame2=1;
				 }             
				else if(Usart_Receive != FRAME_HEADER1 )
				{
					Count=0;
					check_frame1=0;
					check_frame2=0;
				}           
			}
			else 
			{
				Count=0;
				check_frame1=0;
				check_frame2=0;
			}
			
				
			if(Count==2)//获取长度
			{
				data_len=rxbuf[0]<<8|rxbuf[1];
			}
       
			if (Count == data_len && data_len>0) //接收所有数据
			{          
					check=rxbuf[data_len - 2]|rxbuf[data_len - 1]<<8;
					//校验
					if(check == CRC16(&rxbuf[2],data_len -4))
					{   
						pageIndex=rxbuf[2]<<8|rxbuf[3];
						bagIndex=rxbuf[4]<<8|rxbuf[5];
						//sumBag=rxbuf[6]<<8|rxbuf[7];
						
						
						//写入音频到flash  0*512k:欢迎使用  1M:音量加  2M:音量减	
						writeAddrFlash=512*pageIndex*1024 +bagIndex*4096;
						W25QXX_Erase_Sector(writeAddrFlash/4096);//擦除这个扇区
						W25QXX_Write_NoCheck(&rxbuf[8],writeAddrFlash,4096);//写入整个扇区  
						delay_ms(5);
						
						for(j=0; j < 7; j++)
						{
								usart1_send(sendbuf[j]);//回复收到
						}
					}
					Count=0;
					check_frame1=0;
					check_frame2=0;
			}
         
        
    }
    return 0;    
}

void usart1_send(u8 data)
{
    USART1->DR = data;
    while((USART1->SR&0x40)==0);    
}

